 package ads.fafica.modelo;

public class Problema{
	
	private int    codigoProlema;
	private String tipoProblema;
	private String descricaoProblema;
	private String statusProblema;
	private String dtAberturaProblema;
	private String hrAberturaProblema;
	private String latitude;
	private String longitude;
	private String cidade;
	private String bairro;
	private String rua;
	
	
	public int getCodigoProlema() {
		return codigoProlema;
	}
	public void setCodigoProlema(int codigoProlema) {
		this.codigoProlema = codigoProlema;
	}
	public String getTipoProblema() {
		return tipoProblema;
	}
	public void setTipoProblema(String tipoProblema) {
		this.tipoProblema = tipoProblema;
	}
	public String getDescricaoProblema() {
		return descricaoProblema;
	}
	public void setDescricaoProblema(String descricaoProblema) {
		this.descricaoProblema = descricaoProblema;
	}
	public String getStatusProblema() {
		return statusProblema;
	}
	public void setStatusProblema(String statusProblema) {
		this.statusProblema = statusProblema;
	}
	public String getDtAberturaProblema() {
		return dtAberturaProblema;
	}
	public void setDtAberturaProblema(String dtAberturaProblema) {
		this.dtAberturaProblema = dtAberturaProblema;
	}
	public String getHrAberturaProblema() {
		return hrAberturaProblema;
	}
	public void setHrAberturaProblema(String hrAberturaProblema) {
		this.hrAberturaProblema = hrAberturaProblema;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}	
	
}
